package com.tlite.pojo;

public class DefaultLeadSetting {
	
	private int defaultLeadSettingId=0;
	private int timeout=0;
	private int maxBuyers=0;
	public int getDefaultLeadSettingId() {
		return defaultLeadSettingId;
	}
	public void setDefaultLeadSettingId(int defaultLeadSettingId) {
		this.defaultLeadSettingId = defaultLeadSettingId;
	}
	public int getTimeout() {
		return timeout;
	}
	public void setTimeout(int timeout) {
		this.timeout = timeout;
	}
	public int getMaxBuyers() {
		return maxBuyers;
	}
	public void setMaxBuyers(int maxBuyers) {
		this.maxBuyers = maxBuyers;
	}
	
	

}
