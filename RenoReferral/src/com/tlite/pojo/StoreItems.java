package com.tlite.pojo;

public class StoreItems {

	private int store_item_id;
	private int stores_id;
	private String item_name;
	public int getStore_item_id() {
		return store_item_id;
	}
	public void setStore_item_id(int store_item_id) {
		this.store_item_id = store_item_id;
	}
	public int getStores_id() {
		return stores_id;
	}
	public void setStores_id(int stores_id) {
		this.stores_id = stores_id;
	}
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	
	
}
