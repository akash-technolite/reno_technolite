package com.tlite.pojo;

public class BudgetRanges {

	private int budget_ranges_id;
	private int min_value;
	private int max_value;
	public int getBudget_ranges_id() {
		return budget_ranges_id;
	}
	public void setBudget_ranges_id(int budget_ranges_id) {
		this.budget_ranges_id = budget_ranges_id;
	}
	public int getMin_value() {
		return min_value;
	}
	public void setMin_value(int min_value) {
		this.min_value = min_value;
	}
	public int getMax_value() {
		return max_value;
	}
	public void setMax_value(int max_value) {
		this.max_value = max_value;
	}
	
	
}
